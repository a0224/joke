package cc.joke.http;

/**
 * 网络请求客户端参数名称
 * 
 * @author Leo (2013-04-27)
 */
public class RequestParam
{
    /**
     * 运营参数名称
     */
    public static final String OP_PARAM = "op_param";

    /**
     * 唯一设备号
     */
    public static final String OP_DEVICE_ID = "deviceid";

    /**
     * 手机型号名称
     */
    public static final String MODEL = "model";

    /**
     * IMSI标识名称
     */
    public static final String IMSI = "imsi";

    /**
     * IMEI标识名称
     */
    public static final String IMEI = "imei";

    /**
     * 网络类型名称
     */
    public static final String NETWORK_TYPE = "networkType";

    /**
     * 手机设备客户端唯一标识名称
     */
    public static final String DEVICE_ID = "deviceId";

    /**
     * 项目ID名称
     */
    public static final String PROJECT_ID = "projectId";

    /**
     * 手机号码名称
     */
    public static final String PHONE = "phone";

    /**
     * 是否系统程序(应用)名称
     */
    public static final String SYSTEM_STATUS = "systemStatus";

    /**
     * 系统版本号
     */
    public static final String OS_VERSION = "osVersion";

    /**
     * 程序内部版本标识(便于以后程序小升级更新后的区别)
     */
    public static final String APP_VERSION = "appVersion";

    /**
     * 设备宽度像素
     */
    public static final String SCREEN_WIDTH = "screenWidth";

    /**
     * 设备高度像素
     */
    public static final String SCREEN_HEIGHT = "screenHeight";
}
